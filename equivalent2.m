function [Seq, df_ds, d2_f2]=equivalent2(S)
S1=S(1);
S2=S(2);
S3=S(3);
S4=S(4);
Seq=sqrt(S4.^2.*3.0+(S1-S2).^2.*(1.0./2.0)+(S1-S3).^2.*(1.0./2.0)+(S2-S3).^2.*(1.0./2.0));
df_ds=[S1.*2.0-S2-S3;-S1+S2.*2.0-S3;-S1-S2+S3.*2.0;S4.*6.0];
d2_f2=reshape([2.0,-1.0,-1.0,0.0,-1.0,2.0,-1.0,0.0,-1.0,-1.0,2.0,0.0,0.0,0.0,0.0,6.0],[4,4]);